def distance_between_cars(V1, V2, S, t):
    # Расстояние между автомобилями через t часов после опережения
    distance = ((V1 * t) - S) - (V2 * t)
    return distance

V1 = float(input("Введите скорость первого автомобиля (км/ч): "))
V2 = float(input("Введите скорость второго автомобиля (км/ч): "))
S = float(input("Введите расстояние на которое первый автомобиль опережил второй (км): "))
t = 0.5  # Время в часах (30 мин = 0.5 часа)

result = distance_between_cars(V1, V2, S, t)
print(f"Расстояние между автомобилями через 30 минут после опережения: {abs(result)} км")